    //开启提交
  function openCommit(){
    $('#btn').removeClass('close_commit');
  }
	$(function(){
      $('#allow').on('click',function(){
        if($('#more').css('display')=='none'){
          $('#more').show('slow');
          $(this).addClass("trans");
        }else{
          $('#more').hide('slow');
          $(this).removeClass("trans");
        }
      });
      function hideAlert(){
        $('#alert').hide();
      }
      $('#btn').on('click',function(){
         //判断按钮是否关闭
        if($(this).hasClass('close_commit')){
          return;
        }
        var re = /^1\d{10}$/;
        var tel = $('#tel').val();
        var name = $('#name').val();
      if(tel == ''||name == ''){
        $('#alert').html('请填写手机号与姓名').show();
        window.setTimeout(hideAlert,3000);
      }else if (!re.test(tel)){
              $('#alert').html("请输入正确的电话号码").show();
              window.setTimeout(hideAlert,3000);
          }else{
              var data = {
                tel:tel,
                name:name
              };
              //关闭提交按钮
              $('#btn').addClass("close_commit");
              $.ajax({
                        type: "POST",
                        url: "<?php echo site_url('activity/save');?>",
                        dataType: "json",
                        data:data,
                        success:function(data){
                              $('#alert').html(data.msg).show();
                              window.setTimeout(hideAlert,3000);
                              window.setTimeout(openCommit,3000);

                        }
                      });
          }
        
      })
  })
